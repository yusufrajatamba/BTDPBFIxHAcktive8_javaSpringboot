public class newExample {

    public static void main(String[] args) {
        int x =20;
        int y=40;

        System.out.println("x + y = " + (x + y) );
        System.out.println("x - y= " + (x-y));
        System.out.println("x * y= " + (x*y));
        System.out.println("x / y= " + (x/y));
        System.out.println("x % y= " + (x%y));
        System.out.println("x++ =" + (x++));
        System.out.println("x--= " + (x--));

        //Perbedaan dengan x++ dan ++x

        System.out.println("x++  = "+ (x++));
        System.out.println("++x  = "+ (++x));
        
    }
    
}
