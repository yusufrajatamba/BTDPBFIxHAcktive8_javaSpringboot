public class OperatorPenugasan {
    public static void main(String[] args) {
        int a;
        int b;

        //pengisian nilai
        a=5;
        b=10;

        //penambahan

        b+=a;

        //sekarang b=15;
        System.out.println("Penambahan : " +b);

        //Pengurangan
        b-=a;
        System.out.println("Penambahan : " +b);

        //perkalian
        b*=a;
        System.out.println("Penambahan : " +b);

        //pembagian
        b/=a;
        System.out.println("Penambahan : " +b);

        //sisabagi
        b%=a;
        System.out.println("Penambahan : " +b);
    }
    
}
