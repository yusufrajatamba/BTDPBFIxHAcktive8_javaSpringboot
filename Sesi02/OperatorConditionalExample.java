public class OperatorConditionalExample {

    public static void main(String[] args) {
        var x=20;
        var y=40;

        System.out.println("x == y : " + (x==y));
        System.out.println("x != y : " + (x!=y));
        System.out.println("x > y : " + (x>y));
        System.out.println("x < y : " + (x<y));
        System.out.println("x >= y : " + (x>=y));
        System.out.println("x <= y : " + (x<=y));
    }
    
}
